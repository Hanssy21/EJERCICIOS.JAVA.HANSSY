// Crear un "diccionario" utilizando un objeto en JavaScript
let coche = {};

// Agregar elementos al objeto
coche["marca"] = "Ford";
coche["color"] = "rojo";
coche["modelo"] = "sedan";
coche["placa"] = "ROS227";

// Mostrar el valor de 'color'
console.log(coche["color"]);

// Cambiar el valor de 'color'
coche["color"] = "verde";
console.log(coche["color"]);

// Mostrar el valor de 'marca'
console.log(coche["marca"]);

// Cambiar el valor de 'marca'
coche["marca"] = "Renault";
console.log(coche["marca"]);

// Mostrar todo el objeto
console.log(coche);